using System;

class DataTypes
{
    static void Main()
    {
        int a = 10;               // Integer
        float b = 20.5f;          // Float
        double c = 30.75;         // Double
        char d = 'A';   
        string ss="new branch";          // Character
        Console.WriteLine(a);
        Console.WriteLine(b);
        Console.WriteLine(c);
        Console.WriteLine(d);
        Console.WriteLine(ss);
    }
}
